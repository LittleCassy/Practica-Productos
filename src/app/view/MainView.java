    private void btn_ActionAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActionAddProductActionPerformed
        // TODO add your handling code here:
        if (checkStatus() || tf_name.getText().length()<3) {
            
            String aux = ftf_priceAdd.getText().replace(",", ".");
            
            if (MainController.insertProduct(tf_name.getText(), Float.parseFloat(aux), cb_Category.getSelectedIndex() + 1)) {
                lb_statusAdd.setForeground(Color.green);
                lb_statusAdd.setText("Product added successfully!");
            } else {
                lb_statusAdd.setForeground(Color.red);
                lb_statusAdd.setText("I'm sorry, the product could not be added. Check everything it's okay and try again.");
            }
        }else{
            lb_statusAdd.setForeground(Color.red);
            lb_statusAdd.setText("Make sure to fill everything!");
        }

    }//GEN-LAST:event_btn_ActionAddProductActionPerformed

    private void btn_ActionRemoveProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActionRemoveProductActionPerformed
        if (checkStatus()) {
            if (MainController.removeProducts(tf_nameRemove.getText())) {
                lb_deleteConfirmed.setForeground(Color.green);
                lb_deleteConfirmed.setText("Product removed successfully!");
            } else {
                lb_deleteConfirmed.setForeground(Color.red);
                lb_deleteConfirmed.setText("I'm sorry, the product could not be removed. Check everything it's okay and try again.");
            }
        }else{
            lb_statusAdd.setForeground(Color.red);
            lb_statusAdd.setText("Make sure to fill everything!");
        }
    }//GEN-LAST:event_btn_ActionRemoveProductActionPerformed

