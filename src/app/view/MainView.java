    public boolean checkStatus() {
        boolean ready = true;

        for (JTextField tf : myFields) {
            if (tf.getText().equals("")) {
                ready = false;
                tf.setBorder(new LineBorder(Color.red));
            } else {
                tf.setBorder(new LineBorder(Color.blue));
            }
        }

        return ready;
    }

    private void gatherTF(Container myContainer) {
        for (Component component : myContainer.getComponents()) {
            if (component instanceof JTextField) {
                myFields.add((JTextField) component);
            }
        }
    }

